Budynek biurowy firmy marten sp. z o.o. wchodzi w skład nowo budowanej siedziby firmy. Wraz z halą produkcyjną oraz zapleczem socjalnym tworzy kompleks o powierzchni ok. 1200 m².

Obiekt zaprojektowany został w konstrukcji szkieletu stropowo-słupowego z usztywniającym trzonem komunikacyjnym. Rozwiązanie to pozwala na dużą elastyczność i swobodę aranżacji przestrzeni biurowej. Liczba stałych przegród konstrukcyjnych zredukowana została do minimum.


