Elewacja jest systemową fasadą szklaną, wykonaną w technologii szkła bezpiecznego o niskim współczynniku przenikania ciepła. Zastosowanie elewacji szklanej pozwala na dobre doświetlenie przestrzeni biurowej, jest także wizytówką firmy, dla której systemy fasad szklanych są głównym obszarem działalności.

Wysunięty nad południową elewacją dach chroni biura przed nadmierną ekspozycją słoneczną, która ma miejsce w godzinach okołopołudniowych w okresie wiosenno-letnim.

Na parterze znajdują się indywidualne biura wraz z towarzyszącymi węzłami sanitarnymi. Na piętrze znajduje się główne biuro projektowe firmy w skład którego wchodzi otwarta przestrzeń biurowa, boksy dla kadry kierowniczej, przestrzeń konferencyjna, recepcja, zaplecze socjalne oraz sanitariaty.

