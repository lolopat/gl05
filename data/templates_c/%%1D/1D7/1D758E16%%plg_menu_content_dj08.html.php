Płynący w zachodniej granicy działki strumień mocno ograniczył swobodę lokalizowania nowego domu. Odsuwając się od wody możliwie daleko, prostokątny rzut rozciągnięto wzdłuż osi N-S.
Budynek przekryto dachem dwuspadowym (20˚), kalenicę układając prostopadle do jeg osi. Dłuższą połać wyeksponowaną na południe, wykorzystano przy lokalizacji okien dachowych, kolektorów słonecznych i paneli fotowoltaicznych.
Z uwagi na grożący okresowymi wezbraniami strumień, budynek wyniesiono ponad teren.
W parterze zaprojektowano  wejście i połączone z ogrodem strefy, gospodarczą i dzienną. Na poddaszu, powtarzając układ z parteru strefę nocną. Dzięki dużym oknom od wschodu, południa i zachodu, dom korzysta ze światła słonecznego przez cały dzień. Elewacja północna pozbawiona jest okien.  
