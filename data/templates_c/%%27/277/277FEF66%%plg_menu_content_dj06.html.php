Niewielkie wymiary działki i gęsta zabudowa w sąsiedztwie wymusiły poszukiwania rozwiązań funkcjonalnych, formalnych i konstrukcyjnych, wyjątkowych dla tego miejsca.
Podział funkcjonalny dzieli się wg kondygnacji. Strefa dzienna z kuchnią, salonem i częścią gospodarczą znajduje się na parterze. Strefa nocna z trzema pokojami znajduje się na piętrze.
