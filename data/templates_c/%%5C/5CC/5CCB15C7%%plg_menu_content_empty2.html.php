Budynek projektowany jest jako parterowy, częściowo podpiwniczony z użytkowym poddaszem. Bryłę i układ funkcjonalny domu zdeterminowały warunki zastane w terenie. Bezpośrednie sąsiedztwo, lasu z jednej strony (wschodniej), strumienia z drugiej (zachodniej), spadek terenu i nieregularny kształt działki sprawiły, że za najodpowiedniejszy uznano obrys w kształcie prostoąta rozciągniętego w osi N-S. Chcąc nawiązać do tradycyjnych form stosowanych w regionie dom przekryto dwuspadowym dachem o nachyleniu 45˚.
