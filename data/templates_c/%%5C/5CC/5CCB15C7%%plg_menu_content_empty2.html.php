Rozkład funkcji jest prosty i czytelny. W piwnicy, zlokalizowanej w środku budynku, pomieszczenie gospodarcze z kotłem CO, zlewem i kabiną prysznicową. Na parterze w centralnej części, nad piwnicą WC i łazienka dostęna z sypialni w części północnej. W południowej części zlokalizowano strefę dzienną z kuchnią, jadalnią i salonem. Na poddaszu, powtarzając układ z parteru, w centralnej części łazienka, dwa pokoje, jeden od północy, jeden od południa i pustka nad salonem.
Główne wejście do budynku na parterze od strony wschodniej. Północną część domu oparto nad terenem tworząc w poziomie piwnicy podcień, z którego prowadzi drugie wejście do budynku. Na poziomie parteru, od zachodu, taras, dostępny z salonu.
W kierunku południa i zachodu, dom otwiera się dużym oknem - świetlikiem i oknami tarasowymi. Elewacje północna i wschodnia (przesłonięta drzewami) są pełniejsze, ilość okien na nich ograniczono do niezbędnego minimum. 

