<h1 style="padding: 0; margin: 0;">Tło</h1>
<br><br>

GL05  jest grupą projektową założoną przez Łukasza Gawlasa i Krzysztofa Leszczyńskiego w 2005 roku. Współpraca przy licznych projektach konkursowych stała się podstawą do obecnej działalności komercyjnej.
<br><br>
Obecnie prowadzimy studio projektowe zajmujące się designem, architekturą oraz planowaniem przestrzennym.
<br><br>
Zajmujemy się projektami na etapie koncepcyjnym, budowlanym oraz wykonawczym. Jako główni projektanci, koordynujemy prace projektowe i współpracujemy z inżynierami branż technicznych.


<br><br><br>
<h1 style="padding: 0; margin: 0;">Praca</h1>
<br><br>

Mamy świadomość wszechobecności architektury w życiu człowieka i jej wpływu na kreowanie otaczającej rzeczywistości.
<br>
Postrzegamy współczesną architekturę jako dziedzinę interdyscyplinarną.
<br>
Do każdego projektu podchodzimy indywidualnie, tworząc rozwiązanie będące odpowiedzią na konkretne zagadnienie.
<br>
Szanujemy historię architektury, dlatego nie tworzymy imitacji.
<br>
Stosujemy zdobycze współczesnej wiedzy technicznej.

