Budynek projektowany jest jako parterowy z użytkowym poddaszem. Bryłę i układ funkcjonalny domu zdeterminowały warunki zastane w terenie. Nieregularny kształt działki, jej zorientowanie na strony świata oraz bezpośrednie sąsiedztwo, w tym cieku wodnego ze strony zachodniej, sprawiły, że za najodpowiedniejszy uznano obrys w kształcie prostokąta rozciągniętego w osi N-S. Budynek przekryto dwuspadowym dachem o nachyleniu 20˚. Dłuższa połać dachu posiada słoneczną ekspozycję południową, co wykorzystano w lokalizacji okien dachowych oraz przewidywanej lokalizacji kolektorów słonecznych
i paneli fotowoltaicznych, które montowane będą w płaszczyźnie tej połaci.
<img src="app/templates/img/empty3/dj0801.jpg">

Rozkład funkcji jest prosty i czytelny. Na parterze w centralnej części znajduje się pomieszczenie techniczne z kotłem CO, WC, kuchnia oraz komunikacja budynku, w tym schody. W południowej części zlokalizowano strefę dzienną z jadalnią i salonem. Od strony północnej znajduje się dodatkowy pokój dostępny bezpośrednio ze strefy wejściowej.
Na poddaszu, powtarzając układ z parteru, w centralnej części łazienka, dwa pokoje, jeden od północy, jeden od południa i pustka nad salonem. Główne wejście do budynku na parterze poprzez wiatrołap od strony wschodniej. Na poziomie parteru, od południa i zachodu, taras, dostępny z salonu.
W kierunku południa, wschodu i zachodu dom otwiera się dużymi oknami, świetlikami i oknami tarasowymi. Elewacja północna pozbawiona jest okien.
