Realizacja
<br><br><br><br>
Grupa przyjaciół postanowiła otworzyć klub muzyczny. Mocno ograniczony budżet i goniący czas skłonił ich do wykorzystania pomieszczeń po byłej restauracji. Mieszczący się w piwnicy lokal miał w zasadzie gotowe zaplecze socjalne i sanitarne. To pozwoliło inwestorom przeznaczyć ich skromne środki głównie w miejsca dla gości i artystów odwiedzających lokal.

Klub to w zasadzie jedna przestrzeń, którą ‘przecinają’ grube ściany piwnicy, co ułatwiło wydzielenie z niej poszczególnych części lokalu i nadało całości kameralnego charakteru. Ze względów funkcjonalnych i akustycznych, serce SZUFLADY, czyli scena, zostało wyróżnione materiałem, kolorem i oświetleniem. 

