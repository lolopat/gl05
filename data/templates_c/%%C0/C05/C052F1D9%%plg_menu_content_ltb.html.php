Kontenerowy Kompleks Mieszkaniowo-Usługowy w Newark. Projekt Dyplomowy (Politechnika Śląska, Wydział Architektury) zakwalifikowany do finału Konkursu im. Z. Majerskiego na Najlepszy Dyplom roku
<br><br><br><br>


Projekt pracy dyplomowej oparty został na wytycznych konkursu ‘Live the BOX’ ogłoszonego przez AIA Newark and Suburban. Celem konkursu była adaptacja standardowego kontenera transportowego ISO 20' i zastosowanie go w projekcie mieszkaniowo-usługowego kompleksu w Newark. Kontenery mogą zostać zastosowane w dowolnej liczbie oraz konfiguracji, mogą także zostać poddane obróbce (wycinanie fragmentów ścian, wstawianie dodatkowych elementów).

<br>
