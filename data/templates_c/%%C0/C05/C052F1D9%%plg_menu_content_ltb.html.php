Program funkcjonalny kompleksu składa się z części usługowej dostępnej z poziomu ulicy oraz mieszkalnej na wyższych kondygnacjach. Zabudowa mieszkalna występuje w formie wież mieszkalnych oraz kontrastującej z nimi niskiej zabudowy dywanowej. Kontenerowe mieszkania posiadają różne konfiguracje i liczbę modułów. Głównym funkcjom towarzyszy dwupoziomowy parking. System komunikacji w kompleksie rozgrywa się na wielu poziomach. Obiekt posiada strefy o różnych stopniach dostępności, od publicznych ogólnodostępnych do prywatnych tylko dla mieszkańców.

