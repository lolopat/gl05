Wybrana na konkurs działka przylega do głównej stacji kolejowej Broad Street Train Station i znajduje się w zasięgu pieszym od ścisłego centrum (Downtown) Newark. Miejsce pełni funkcję węzła komunikacyjnego dla lokalnej społeczności oraz osób dojeżdżających do pracy środkami komunikacji miejskiej. Lokalizacja ta jest idealna dla stworzenia znaku ‘bramy’ w przestrzeni urbanistycznej. 

