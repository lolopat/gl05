<br>
Motywem przewodnim obiektu są cztery smukłe wieże ułożone z kontenerów ISO 20’. Ich powtarzalny rytm potęguje odbiór u obserwatorów znajdujących się w ruchu, przybywających na stację BSTS.
Projekt oparty został na ortogonalnej siatce, podobnie jak cały system transportu, łączenia i magazynowania kontenerów w portach. Taki sposób składowania i przemieszczania kontenerów  wynika z optymalizacji. Przyjęcie tego samego systemu dla wielofuncyjnego kompleksu oprócz  korzyści technicznych jest także ukłonem w stronę dziedzictwa przemysłowego, a zwłaszcza kontenerowego portu Newark..
<br>

