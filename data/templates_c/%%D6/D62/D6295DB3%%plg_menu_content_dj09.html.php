Działka pod budowę domu jest płaska, lekko opadająca na północ. 
Dogodne ułożenie względem kierunków świata i niewielki spadek terenu wykorzystano przy rozkładaniu funkcji w nowym domu.
W przyziemiu (półpiwnicy) zlokalizowano rozległy podcień, wejście, strefę gospodarczą i garaż.
Parter, bezpośrednio połączony z ogrodem,  przeznaczono na kuchnię z jadalnią i duży salon.
Srefę nocną z czterema pokojami zaprojektowano na półpiętrze.
Wszystkie części połaczone są schodami zlokalizowanymi w centralnej części domu, co dało dużo swobody w planowaniu układu pomieszczń i pozwoliło zminimalizować powierzchnię przeznaczoną na komunikację.


