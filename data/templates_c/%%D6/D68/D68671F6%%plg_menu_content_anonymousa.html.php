TŁO
<br>
Zadaniem konkursu <a href='http://www.anonymousd.com/index.php'>anonymous.a</a> było zaprojektowanie tymczasowej struktury/pawilonu wystawowego pod mostem napowietrznej koleji miejskiej, jednym z najbardziej zapomnianych miejsc każdego dużego miasta. Projekt powinien transformować to ciemne i opuszczone miejsce w przestrzeń zgoła innych doświadczeń związanych z ekspozycją projektowanej struktury/pawilonu.
	Przeznaczona na projekt działka znajduje się w Nowym Jorku pod linią “High Line” na ulicy 24 West, w sąsiedztwie wielu znanych na całym świecie galerii. Projekt powinien być rozwinięciem dla ich działalności, zapewniając możliwość zewnętrznej ekspozycji.

