STRUKTURA, MATERIAŁY, MONTAŻ
<br>

	Trójwymiarowa struktura zbudowana jest na siatce trójkątów. Każda płaszczyzna posiada własną zorientowaną strukturę nośną. Jako materiał zastosowano standardowe płyty OSB 2x25mm, zarówno dla konstrukcji jak i wykończenia podłogi. Materiał ten jest tani, łatwy w obróbce, a jego surowy wygląd cieszy się ostatnio dużą popularnością. Budowa przy użyciu tego materiału jest prosta, wymaga tylko podstawowych narzędzi jak wkrętarki oraz wyżynarki. 
	W strukturze poszczególne płaszczyzny pełnią różne fukcje. Jedne z nich funkcjonują jako scena, inne na przeciwko tworzą formę widowni. Pozostałe płaszczyzny służą komunikacji jakkolwiek nie istnieje określony z góry jedyny słuszny sposób użytkowania topografii.
	Nocna iluminacja stworzona jest przy pomocy technologii LED wzdłuż krawędzi styku pomiędzy poszczególnymi płaszczyznami, co podkreśla geometryczny charakter obiektu.

