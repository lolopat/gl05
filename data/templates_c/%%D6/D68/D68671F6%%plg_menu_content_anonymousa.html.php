IDEA
<br>
   Celem naszego projektu jest stworzenie przestrzeni dla wymiany myśli, prezentacji idei oraz wystaw. Podobnie jak Agora w starożytnej Grecji, miejsce powinno przede wszystkim tworzyć  tło dla powyższych aktywności. ‘High Line’ ze swoją nieprawdopodobną konstrukcją oraz bezpośrednim otoczeniem nadają się do tego celu doskonale. Czego tak naprawde brakuje to podłoga...
	Podłoga z różnicą wysokości, która zapewni widzom dobry  widok, a występującym wyeksponowane miejsce. Pochylnie zapewnią komunikację pomiędzy poszczególnymi poziomami oraz dadzą możliwość zajęcia miejsc siedzących. Całości dopełni odpowiedni materiał wykończeniowy.
