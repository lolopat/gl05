Budynek projektowany jest jako piętrowy, niepodpiwniczony. Bryłę domu zdeterminowały warunki zastane na działce. Niewielkie wymiary parceli i gęsta zabudowa w sąsiedztwie zmusiły do poszukiwań rozwiązań funkcjonalnych, formalnych i konstrukcyjnych wyjątkowych dla tego miejsca.
Podział funkcjonalny dzieli się wg kondygnacji. Część mieszkalna, dzienna z kuchnią, salonem, kotłownią i wc, znajduje się na parterze. Część nocna z trzema pokojami i łazienką znajduje się na piętrze. Taras zaprojektowano wzdłuż elewacji południowo – zachodniej. Ze względów energetycznych i funkcjonalnych strefę wejściową, łazienki i pomieszczenie techniczne zlokalizowano w północnej i wschodniej części budynku, a pomieszczenia mieszkalne (przeznaczone na pobyt stały mieszkańców) ułozono od wschodu, południa i zachodu. Taki układ wnętrza ma bezpośrednie przełozenie na formę budynku. 
