Chcąc zminimalizować straty energetyczne, część północna i wschodnia domu jest wyraźnie niższa, a liczba otworów okiennych ograniczona jest do niezbędnego minimum. Wysoki południowo – zachodni narożnik, mocno przeszklony pozwala w ciasnej zabudowie sąsiednich działek, wychwytywać światło słoneczne do późnych godzin popołudniowych, nie tracąc nic nawet w miesiącach zimowych. Tak wymodelowaną bryłę budynku przekrywa niesymetryczny dach dwuspadowy, pokryty blachą. Dom w pełni korzysta z różnorodności otaczającej zabudowy, czerpiąc z jej rozwiązań funkcjonalnych i formalnych.
