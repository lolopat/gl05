MASHRABIYA<br>
   Siatka rozpięta jest pomiędzy kolumnami. Podążające za dziesięciokątną siatką kable niosą ogniwa fotowoltaiczne o kształtach elementów mozaiki girih. Panele rozmieszczone zostały w określonych regułach geometrii, która zawsze odgrywała dużą rolę w lokalnej kulturze. Podczas wędrówki pod tym mozaikowym dachem zmienia się natężenie światła. Od niemal pełnej ekspozycji słonecznej, aż do głębokiego cienia, gdzie słońce rzuca tylko niewielkie przebłyski przywołując motywy maszrabiji. Cała struktura unosi się nad powierzchnią pustyni w bezruchu. Zawieszona, wykonana przez człowieka ‘chmura’ rzuca niezliczone cienie i daje schronienie przed wszechobecnym słońcem.

