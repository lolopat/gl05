SIATKA girih<br>
   Patrząc wstecz na sztuke i architekturę świata arabskiego, nie trudno zauważyć wielu powiązań z geometrią. Wysoko rozwinięta matematyka odcisnęła swoje piętno w innych dziedzinach na wiele sposobów. Geometria jako graficzna reprezentacja matematycznych zasad kształtowała formę architektoniczną i ornamentykę. Ta druga była przeważnie oparta na sześciokątnej lub ortogonalnej siatce, będącej podstawą okresowej tessalacji (podziału powierzchni na wielokąty) płaszczyzny, zdolnej pokrywać całe ściany powtarzalnymi wzorami. Prawdziwa rewolucja przyszła jednak wraz zastosowaniem siatki dziesięciokątnej, która umożliwiła generowanie wzorów nieokresowej tesselacji. Ten system tworzenia wzorów, składający się z kilku typów płytek, nazywany girih, tworzy w rzeczywistości wzór ‘regularnej nieregularności’. Co więcej, girih zachowują się w sposób podobny do fraktali. Kompozycja płytek w danej skali może utworzyć ten sam typ w większej skali.

