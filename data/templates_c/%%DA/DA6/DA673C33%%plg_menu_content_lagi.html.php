MOZAIKA<br>
   W cieniu napowietrznej struktury, przyziemie urządzone jest na podstawie tej samej siatki geometrycznej z rozlokowanymi w jej obrębie elementami sztucznego krajobrazu. System komunikacji i kanałów wodnych opierają się na geometrii wzorów girih. Położenie elementów sztucznego krajobrazu takich jak wieże wodne, centrum komercyjne, stadion sportowy czy amfiteatr, wynikają z zastosowania siatki. Na obszarze projektu stworzono wiele rodzajów krajobrazu. Dominującym typem jest pustynia wraz z występującą na niej ubogą roślinnością. 

