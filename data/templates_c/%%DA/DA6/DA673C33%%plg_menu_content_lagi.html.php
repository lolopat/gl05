ENERGIA SŁOŃCA<br>

Trudno porównywać ilość energii produkowanej przez Słońce z jakimkolwiek źródłem energii stworzonym do tej pory przez człowieka. W ciągu jednej tylko godziny Słońce dostarcza Ziemii energię równą rocznemu światowemu zapotrzebowaniu.  Ten naturalny reaktor termojądrowy bombarduje każdy metr kwadratowy powierzchni Ziemii energią równą 1200W. Pozostaje tylko jedno pytanie... w jaki sposób ją pozyskać.

<img src="app/templates/img/lagi/lagi06.jpg">
	
	
ELEKTROWNIA SŁONECZNA<br>

   Zlokalizowana w konkretnym punkcie na powierzchni Ziemii, elektrownia słoneczna musi borykać się z okresowym brakiem energii nie tylko podczas nocy ale także w pochmurne dni. Jej chwilowa moc jest proporcjonalna do ilości odbieranego światła słonecznego. Ale nie tylko wielkość produkcji energii zmienia się w czasie.

<br>
	
DZIENNE ZAPOTRZEBOWANIE NA ENERGIĘ ELEKTRYCZNĄ<br>

   Zapotrzebowanie na energię eletryczną zmienia się w ciągu doby, co ma związek z aktywnością człowieka podczas 24-godzinnego cyklu. Najmniejsze zapotrzebowanie na energię ma miejsce około 3 nad ranem. Od tego momentu zapotrzebowanie stale rośnie, aż do szczytu o godzinie 13. Następnie zapotrzebowanie lekko spada, pozostając jednak na wysokim poziomie aż do godziny 20, kiedy osiągnięty zostaje wieczorny szczyt, po którym gwałtownie spada.
	
<br>

REDYSTRYBUCJA ENERGII<br>

   Jak pogodzić rozkład produkcji energii elektrycznej elektrowni słonecznej z rozkładem dobowego zapotrzebowania na prąd? To jest prawdziwe zagadnienie i wyzwanie.

<br>


MAGAZYN ENERGII ELEKTRYCZNEJ <br>
   Główną cechą nadprzewodników jest całkowity brak występowania oporu przewodzonia prądu elektrycznego. W konsekwencji tego zjawiska, w pierścieniu wykonanym z nadprzewodnika, prąd elektryczny może płynąć dowolnie długo. Fakt ten sprawia, że nadprzewodniki są potencjalnie doskonałą metodą przechowywania energii elektrycznej. W rzeczywistości jest to jedyna metoda, która przechowuje prąd elektryczny w sposób bezpośredni. Niestety technologia nadprzewodników jest we wczesnej fazie rozwoju i nadal potrzebuje bardzo niskich temperatur do pracy, co czyni ją bardzo drogą. Wracając do rzeczywistości, obecnie istnieje pare systemów służących przechowywaniu energii elektrycznej stosowanych z mniejszym lub większym sukcesem. W przeciwieństwie do potencjalnych możliwości nadprzewodnictwa, najpierw energia elektryczna zamieniana jest na inny rodzaj energii jak cieplna, kinetyczna, potencjalna sprężystości lub grawitacji, czy też energia chemiczna a następnie w tej postaci przechowywana. Na żadanie następuje odwrócenie tego procesu, podczas którego energia elektryczna jest z pewnymi stratami odzyskiwana.

<br>
ELEKTROWNIA SZCZYTOWO POMPOWA<br>
   Użycie  potencjalnej siły grawitacji do magazynowania energii wymaga działania na relatywnie dużych masach. Niemniej jednak ma ono niewątpliwe zalety, zwłaszcza jeśli czynnik roboczy jest powszechnie dostępny.Ten sposób magazynowania energii ma miejsce w elektrowniach szczytowo-pompowych, gdzie czynnikiem roboczym jest woda. Pompowana z dolnego zbiornika do górnego woda przechowuje energię potencjalną. Kiedy kierunek przepływu jest odwracany, turbiny wodne, które w pierwszej fazie pracowały jako pompy, zamieniają się w wysokowydajne generatory energii elektrycznej. Wydajność przechowywania energii elektrycznej w ten sposób sięga 80% niezależnie od czasu przechowywania. Powyższy proces wykorzystuje zjawiska czysto fizyczne, nie ma wpływu na właściwości chemiczne wody ani nie powoduje zanieczyszczenia. Ilość zmagazynowanej w ten sposób energii jest ograniczona tylko wielkością dwóch zbiorników wody.

<br>

FOTOWOLTAIKI<br>
   Ogniwa fotowoltaiczne są najbardziej popularnym systemem zamiany energii słonecznej na energię elektryczną. W przeciwieństwie do termicznej elektrowni słonecznej ogniwa fotowoltaiczne nie wymagają użycia ruchomych części takich jak lustra, heliostaty, obiegi wody, pary, turbiny czy generatory. Elektrownie fotowoltaiczne zamieniają energię słoneczną bezpośrednio na prąd stały, przy sprawności wahającej się obecnie w przedziale od 12% - 23%. Fotowoltaiki są powiązane bezpośrednio z technologiami kosmicznymi, gdzie sięga ich geneza. Prostota budowy ogniw wpłynęła na ich rozpowszechnienie jako niezależne źródła prądu dla rozmaitych urządzeń przenośnych, pojazdów czy budynków. Można je łączyć na wiele sposobów z architekturą.

