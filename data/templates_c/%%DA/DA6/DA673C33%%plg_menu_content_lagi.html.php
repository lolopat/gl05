WODNE WIEŻE<br>
Cztery wysmukłe wieże wznoszą się nad poziom fotowoltaicznej mozaiki. Dominując nad okolicą podkreślają szczególną funkcję jaka przypada im w całej instalacji. Te wodne wieże są w rzeczywistości mini elektrowniami szczytowo-pompowymi, zdolnymi do przechowywania energii elektrycznej wytworzonej przez fotowoltaiki. Zwracają one zmagazynowaną energię elektryczną w czasie, gdy fotowoltaiki przestają ją wytwarzać. Podczas dziennego cyklu, tysiące litrów wody przemieszczają się z górnego zbiornika do dolnego i z powrotem. Wszystkie dolne rezerwuary wody połączone są ze sobą kanałami, których wąskie i głębokie przekroje minimalizują parowanie. Cały system tworzy układ naczyń połączonych i zasilany jest wodą z zatoki. Głównym celem wodnych wież jest magazynowanie energii elektrycznej, niemniej ubocznym efektem ich pracy jest wytworzenie prądów wodnych w kanałach. Zjawisko to wykorzystano projektując na jednym odcinku tor kajakarstwa górskiego. Na szczycie każdej wieży zlokalizowana jest platforma widokowa. Konstrukcja wież oparta jest na hiperboloidzie, którą charakteryzuje wysoka wytrzymałość i niskie zużycie materiału.




