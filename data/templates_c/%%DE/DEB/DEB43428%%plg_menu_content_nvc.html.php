Newark Visitors Center Competition
<br><br><br><br>

Teren przeznaczony na projekt znajduje się pomiędzy tkanką miejską Newark a obszarami rekreacyjnymi miasta mieszczącymi się wzdłuż rzeki Passaic. Ta charakterystyczna lokalizacja sprzyja funkcji obiektu jako bramy wejściowej do nadrzecznego parku Newark - New Riverfront Park of Newark.
<BR>
Budynek zaprojektowany został w oparciu o siatkę heksagonalną trójkątów równobocznych. Każdy komponent budynku NVC oparty jest na pojedynczym  module siatki lub jego wielokrotności. Siatka heksagonalna pozwala na tworzenie urbanistyki opartej na trzech kierunkach, co daje dodatkowe możliwości w stosunu do siatki ortogonalnej opartej na dwóch kierunkach. Podstawowy moduł tej siatki czyli  trójkąt jest elementem niezmiennym geometrycznie, doskonałym z puntu widzenia konstrukcyjnego. Powtarzalna geometria zwiększa skalę w jakiej postrzegany jest ten niewielki obiekt w kontekscie wielkoskalowego centrum.
<BR>
Projektowany budynek został podzielony na trzy bloki funkcjonalne oddzielone pustą przestrzenią. Pierwszy blok zawiera audytorium, salę konferencyjną oraz foyer z funkcjami towarzyszącymi. Elementy stropowe tworzą amfiteatr zwrócony w kierunku rzeki. Drugi blok zawiera galerie. Znajdują się w nim stała ekspozycja oraz wędrujące wystawy z innych galerii jak również kafejka z tarasem z widokiem na rzekę. Ostatni trzeci blok jest częścią komercyjną i zawiera powierzchnię handlową, m.in. sklep z pamiątkami. Wszystkie trzy bloki połączone są częścią podziemną, gdzie znajduje się parking oraz pomieszczenia techniczne. Przekryte tekstylnymi membranami wolne przestrzenie pomiędzy blokami służą jako przestrzeń komunikacyjna i zapewniają łatwy dostęp do nadrzecznego parku. Budynek NVC staje się częścią założenia New Riverfront Park.
