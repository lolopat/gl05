Projekt koncepcyjny krytych kortow tenisowych

<br><br><br><br>

Zamierzeniem inwestora jest stworzenie ośrodka oferującego pełny zakres usług związanych z rekreacyjnym i profesjonalnym uprawianiem tenisa przez cały rok. Wstępne założenia określiły termiarz i program funkcjonalny inwestycji dzieląc ją na dwa etapy. W pierwszym powstanie hala przekrywająca 4 korty i budynek gospodarczo - usługowy mieszczący magazyn, zaplecze sanitarne, siłownię i część gastonomiczno - usługową. Dodatkowo 2 korty powstaną na dachu hali. Etap drugi obejmuje rozbudowę kompleksu o 4 korty ziemne, otwarte.

<img src="app/templates/img/korty/korty02.jpg">

Działka, którą dysponuje inwestor, to teren dawnej piaskowni. Jego specyfika; różnice poziomów sięgające 6 metrów, niestabilne grunty i położenie na obszarze górniczym (2 kat.) bezpośrednio przekładają sie na zastosowane rozwiązania konstrukcyjne i układ funkcjonalny.
Dach nad kortami ułożony zostanie na drewnianych dźwigarach kratowych opartych na siatce słupów żelbetowych. Dwukondygnacyjny budynek zaplecza przylegający do hali, wtopiony w skarpę, swoim przyziemiem spełni rolę ściany oporowej. Całkowicie przeszklona druga kondygnacja pozwoli wpuścić do hali południowe światlo.
Korty na dachu dostępne będa od zewnątrz, schodami biegnącymi z tarasu kawiarni.


