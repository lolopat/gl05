Bryłę i układ funkcjonalny domu zdeterminowały warunki zastane na działce. Bezpośrednie sąsiedztwo, lasu z jednej  (E) i strumienia z drugiej (W) strony, spadek i nieregularny kształt terenu sprawiły, że za najodpowiedniejszy uznano rzut na porostokącie rozciągniętym w osi N-S.
Rozkład funkcji jest prosty i czytelny. W przyziemiu (piwnicy), podcień, wejście i pomieszczenie gospodarcze. Na parterze sypialnia i połączone z ogrodem, kuchnia i salon. Na poddaszu, powtarzając układ z parteru, dwa pokoje. Dom otwiera się w kierunku południa i zachodu, a elewacje północna i wschodnia (przesłonięte drzewami) są pełniejsze i ilość okien na nich ograniczono do niezbędnego minimum. 

