<br><br><br><br>

DESCRIPTION                                            The main objective of this project is not only to create full-functional houses on the water, but to create the public space for them as well. By enriching water houses with the common space over all components, we created an artificial island. Thus we get a piece of 'land' in the enviroment that normally would lack it.          Public space is upon different functions. It allows to communicate easily between all the components. You can go visit your neighbours, go down to the platform by the water or simply return to the real land. This enermous consolidated surface of roofs-terraces is also a perfect place for recreation, social meetings, sunbathing and contemplating the surroundings. The roof terrace with its great isolation separates the public space on it from the private below.                                                    Private space is located by the water. Wide terraces partly located under cannopy protect from sun and make easy approach to water. You can moor your boat here or take a bath.        There are 4 unit types in the project: A, B, C and D. Type A is a main flat, with area of 123 square metres. Glass walls on both sides of the house make it bright and airy.

